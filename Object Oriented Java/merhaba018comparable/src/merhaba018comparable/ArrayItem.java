package merhaba018comparable;

public class ArrayItem {
    // Dizi elemaný olarak kullanýlacak olan sýnýfý ifade eder.
    // Ýsim alanýdýr.
    private String name;
    // Yaþ alaný, bu alan sýralamaya tabi tutulacaktýr.
    private int age;
    
    public ArrayItem() {
          
    }
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;    
    }
}
